package jsj.hhtc.base;

public class Member {
    String name;
    String department;
    String grade;
    String classNumber;
    String ipAddress;

    Member(String name, String department, String grade, String classNumber, String ipAddress) {
        this.name = name;
        this.department = department;
        this.grade = grade;
        this.classNumber = classNumber;
        this.ipAddress = ipAddress;
    }

}
